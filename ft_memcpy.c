void	*memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		*(char *)dest = *(const char *)src;
		i++;
		src++;
		dest++;
	}
	return ((void *)dest);
}
